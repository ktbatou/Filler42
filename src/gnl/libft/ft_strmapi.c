#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	int				j;
	char			*str;

	if (!(s))
		return (NULL);
	i = 0;
	j = ft_strlen(s) + 1;
	str = (char *)malloc(sizeof(char) * j);
	if (str == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
